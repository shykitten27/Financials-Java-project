            private void jbtnSchedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSchedActionPerformed
                NumberFormat c = NumberFormat.getCurrencyInstance();
                NumberFormat p = NumberFormat.getPercentInstance();
                p.setMaximumFractionDigits(3);
                JTable tbl;
                DefaultTableModel mod;
                String title = "";

                if(jradAnnuity.isSelected()) {
                String [] columns = {"Month", "Beg. Bal.", "Deposit", "Rate", "Int. Earned", "End. Bal."};
                String[][] t = new String[a.getTerm()][6];
                mod = new DefaultTableModel(t,columns);
                tbl = new JTable(mod);
                DefaultTableCellRenderer rend = new DefaultTableCellRenderer();
                rend.setHorizontalAlignment(JLabel.RIGHT);
                tbl.getColumnModel().getColumn(0).setCellRenderer(rend);
                tbl.getColumnModel().getColumn(1).setCellRenderer(rend);
                tbl.getColumnModel().getColumn(2).setCellRenderer(rend);
                tbl.getColumnModel().getColumn(3).setCellRenderer(rend);
                tbl.getColumnModel().getColumn(4).setCellRenderer(rend);
                tbl.getColumnModel().getColumn(5).setCellRenderer(rend);
                
                //fill table
                for (int i=0; i<a.getTerm(); i++) {
                    tbl.setValueAt((i+1), i, 0);
                    tbl.setValueAt(c.format(a.getBeginBal(i+1)), i, 1);
                    tbl.setValueAt(c.format(a.getAmount()), i, 2);
                    tbl.setValueAt(p.format(a.getRate()), i, 3);
                    tbl.setValueAt(c.format(a.getIntEarned(i+1)), i, 4);
                    tbl.setValueAt(c.format(a.getEndBal(i+1)), i, 5);
                }
                title = "Annuity Schedule";

            } else {
            //loan values go here
                String[] column = {"Month", "Beg.Bal.", "Payment", "Int. Chg.", "End. Bal"};
                String[][] t = new String[ln.getTerm()][5];
                mod = new DefaultTableModel(t,column);
                tbl = new JTable(mod);
                DefaultTableCellRenderer rend = new DefaultTableCellRenderer();
                rend.setHorizontalAlignment(JLabel.RIGHT);
                tbl.getColumnModel().getColumn(0).setCellRenderer(rend);
                tbl.getColumnModel().getColumn(1).setCellRenderer(rend);
                tbl.getColumnModel().getColumn(2).setCellRenderer(rend);
                tbl.getColumnModel().getColumn(3).setCellRenderer(rend);
                tbl.getColumnModel().getColumn(4).setCellRenderer(rend);

                //fill tbl with values from ln object...
                for (int i=0; i<ln.getTerm(); i++) {
                    tbl.setValueAt((i+1), i, 0); //month or term
                    tbl.setValueAt(c.format(ln.getBeginBal(i+1)), i, 1);
                    tbl.setValueAt(c.format(ln.getMoPmt()), i, 2);
                    tbl.setValueAt(c.format(ln.getIntCharged(i+1)), i, 3);
                    tbl.setValueAt(c.format(ln.getEndBal(i+1)), i, 4);
                }

                title = "Loan Schedule";
            }

             
            JScrollPane sp = new JScrollPane(tbl);
            JDialog dg = new JDialog();
            dg.add(sp);
            dg.setBounds(150,400,400,300);
            dg.setTitle(title);
            dg.setVisible(true);

            }//GEN-LAST:event_jbtnSchedActionPerformed

