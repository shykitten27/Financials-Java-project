    private void jbtnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCalcActionPerformed
        double amt, rate;
        int term;
        NumberFormat f = NumberFormat.getCurrencyInstance();
        statusMessageLabel.setText("");
        try {
            amt = Double.parseDouble(jtxtAmt.getText());
            if (amt <= 0) {
                statusMessageLabel.setText("Please enter a positive amount");
                return;
            }
        } catch (Exception e) {
            statusMessageLabel.setText("Amount is not numeric - please re-enter");
            jtxtAmt.requestFocusInWindow();
            return;
        }
        try {
            rate = Double.parseDouble(jtxtRate.getText());
            if (rate <= 0 || rate > 1) {
                statusMessageLabel.setText("Please enter a rate between 0 and 1.0");
                return;
            }
        } catch (Exception e) {
            statusMessageLabel.setText("Rate is not numeric - please re-enter");
            jtxtRate.requestFocusInWindow();
            return;
        }
        try {
            term = Integer.parseInt(jtxtTerm.getText());
            if (term <= 0) {
                statusMessageLabel.setText("Please enter a positive value for term");
                return;
            }
        } catch (Exception e) {
            statusMessageLabel.setText("Term is not numeric - please re-enter");
            jtxtTerm.requestFocusInWindow();
            return;
        }
        if(jradAnnuity.isSelected()) {
            a = new Annuity();
            a.setAmount(amt);
            a.setRate(rate);
            a.setTerm(term);
            double fv = a.getFinalValue();
            jtxtResult.setText(f.format(fv));

    }else{
            //loan is calculated
            ln = new Loan(amt,rate,term);
            jtxtResult.setText(f.format(ln.getMoPmt()));


    }
            jbtnSched.setEnabled(true);

    }//GEN-LAST:event_jbtnCalcActionPerformed

